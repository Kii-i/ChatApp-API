export const fifteenMinutesFromNow = () =>
  new Date(Date.now() + 1000 * 60 * 15);
export const thirtyDaysFromNow = () =>
  new Date(Date.now() + 1000 * 60 * 60 * 24 * 30);
export const thirtyDaysFromNowInSeconds = () => 60 * 60 * 24 * 30;
export const tenMinutesFromNowInSeconds = () => 60 * 10;
export const fiveMinutesFromNowInSeconds = () => 60 * 5;
